#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;
	t_list	*temp;
	t_list	*data;

	if (!lst || !f)
		return (NULL);
	new = NULL;
	while (lst)
	{
		data = f(lst->content);
		temp = ft_lstnew(data);
		if (!temp)
		{
			free(data);
			ft_lstclear(&new, del);
			return (NULL);
		}
		ft_lstadd_back(&new, temp);
		lst = lst->next;
	}
	return (new);
}
